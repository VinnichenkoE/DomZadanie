package homework_lesson4.task2;

public class Window {
    double windowLenght;
    double windowWidth;
    public Window(double windowLenght, double windowWidth){
        this.windowLenght = windowLenght;
        this.windowWidth = windowWidth;
    }
    public double GetWindowLenght(){
        return windowLenght;
    }
    public double GetWindowWidth(){
        return windowWidth;
    }
}
