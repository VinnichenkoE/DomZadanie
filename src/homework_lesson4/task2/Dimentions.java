package homework_lesson4.task2;

public class Dimentions {
    double width;
    double lenght;
    double deep;

    public  Dimentions(double width,double lenght,double deep){
        this.width = width;
        this.lenght = lenght;
        this.deep = deep;
    }
    public double GetWidth(){
        return width;
    }
    public double GetLenght(){
        return lenght;
    }
    public double GetDeep(){
        return deep;
    }
}
